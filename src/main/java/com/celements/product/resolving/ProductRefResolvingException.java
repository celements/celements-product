public class ProductRefResolvingException extends Exception {

  private static final long serialVersionUID = 1L;

  public ProductRefResolvingException() {
    super();
  }

  public ProductRefResolvingException(String msg) {
    super(msg);
  }

  public ProductRefResolvingException(Throwable cause) {
    super(cause);
  }

  public ProductRefResolvingException(String msg, Throwable cause) {
    super(msg, cause);
  }

}
